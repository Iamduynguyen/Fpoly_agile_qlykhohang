    private void setCol() {
        String[] arr = {"STT", "Tên đăng nhập", "Tên người dùng", "Trạng thái", "Chức vụ"};
        for (String x : arr) {
            model.addColumn(x);
        }
    }

    protected void loadTable() {
        model.setRowCount(0);
        int stt = 0;
        for (User x : iLogin.getListUser()) {
            if (!x.getUsername().equalsIgnoreCase(_User.getUsername())) {
                model.addRow(new Object[]{
                    ++stt, x.getUsername(), x.getName(),
                    x.isStatus() ? "Hoạt động" : "Khóa",
                    iLogin.getNameRole(x.getRole())
                });
            }
        }
    }

