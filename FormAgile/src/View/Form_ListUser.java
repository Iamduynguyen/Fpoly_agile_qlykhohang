    private void btn_khoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_khoaActionPerformed
        String inputUsername = "";
        if (indexClickTable == tbDanhSach.getSelectedRow()) {
            inputUsername = tbDanhSach.getValueAt(indexClickTable, 1).toString();
        } else {
            inputUsername = JOptionPane.showInputDialog("Nhập tài khoản đăng nhập muốn xóa!");
        }
        if (inputUsername == null || inputUsername.isEmpty()) {
            return;
        }
        if (inputUsername.equalsIgnoreCase(_User.getUsername())) {
            JOptionPane.showMessageDialog(this, "Không thể tự khóa tài khoản của bạn!");
            return;
        }
        if (JOptionPane.showConfirmDialog(this, "Đồng ý khoá người dùng " + inputUsername.toUpperCase(), "Khóa người dùng",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            User user = new User();
            user = iLogin.getUser(inputUsername);
            if (user != null) {
                user.setStatus(false);
                if (iLogin.editAccount(user)) {
                    JOptionPane.showMessageDialog(this, "Khóa thành công!");
                     Service._ILogin = new Login();
                    loadTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Khóa thất bại!");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Người dùng không tồn tại!");
            }
        }
    }//GEN-LAST:event_btn_khoaActionPerformed

